package cn.rongcloud.ktvmusickit.model;

public class MixConfig {
    /**
     * 弹框中的混响效果类型
     */
    public int mixTypeIndex = 0;

    /**
     * 伴奏音量
     */
    public int styleVolume = 100;

    /**
     * 人声音量
     */
    public int vocalVolume = 100;

    /**
     * 升降调
     */
    public int liftScale = 0;

    /**
     * 是否耳返
     */
    public boolean ear = false;

    /**
     * 是否开启音准器
     */
    public boolean intonation = false;
}
